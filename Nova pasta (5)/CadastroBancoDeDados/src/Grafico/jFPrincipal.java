    public void readJTable() {
        DefaultTableModel dtmCliente = (DefaultTableModel) jTPrincipal.getModel();
        ClienteDAO cd = new ClienteDAO();

        for (ClienteModelo c : cd.ler(login)) {
            
            this.idLogin = c.getId();
            
            dtmCliente.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getCpf(),
                c.getRg(),
                c.getData_nasc(),
                c.getLogin(),
                c.getSenha()
            });
        }
    }
    
     public void readJTableP() {
        DefaultTableModel dtmProduto = (DefaultTableModel) jTProduto.getModel();
        dtmProduto.setNumRows(0);
        ProdutoDAO pd = new ProdutoDAO();

        for (ProdutoModelo pm : pd.ler(login)) {
            dtmProduto.addRow(new Object[]{
                pm.getId_prod(),
                pm.getNome(),
                pm.getPreco()
            });
        }
    }
    

