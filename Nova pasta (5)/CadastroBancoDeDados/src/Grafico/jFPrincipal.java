    private void jBTNSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTNSalvarActionPerformed
       
        boolean ok = true;
        String nome = jTFNomeP.getText();
        String nomeU = nome.toUpperCase();
        

        if (jTFNomeP.getText().equals("")
         ||(jFPPreco.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Campo em branco");
            ok = false;
        } else {
        
        if (ok == true){
        Double preco = Double.parseDouble(jFPPreco.getText());
            ProdutoDAO pd = new ProdutoDAO();
        ProdutoModelo pm = new ProdutoModelo();
        pm.setNome(nomeU);
        pm.setId(idLogin);
        pm.setPreco(preco);
        pd.criar(pm);
        readJTableP();
        }
        }
    }//GEN-LAST:event_jBTNSalvarActionPerformed

