public class Tag {
    
    private int id;
    private String nome_tag;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome_tag() {
        return nome_tag;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome_tag(String nome) {
        this.nome_tag = nome;
    }
    
}
